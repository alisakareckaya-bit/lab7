package common;

/**
 * Утилитный класс для генерации уникальных идентификаторов объектов.
 * Обеспечивает сквозную нумерацию элементов коллекции в рамках текущей сессии.
 *
 * @author Алиса
 * @version 1.0
 */
public class Generatic {

    /** Текущее значение счетчика для следующего ID */
    private static int id = 1;

    /**
     * Генерирует новый уникальный идентификатор.
     * Возвращает текущее значение и инкрементирует счетчик для следующего вызова.
     *
     * @return уникальное целое число (Integer)
     */
    public static Integer generateId() {
        return id++;
    }

    /**
     * Сбрасывает счетчик идентификаторов до начального значения (1).
     * Обычно используется при полной очистке коллекции.
     */
    public static void reset() {
        id = 1;
    }

    /**
     * Устанавливает значение счетчика для генерации следующего ID.
     * Полезно при загрузке существующей коллекции из файла, чтобы избежать дублирования ID.
     *
     * @param nexId новое значение счетчика
     */
    public static void setId(int nexId) {
        id = nexId;
    }
}
