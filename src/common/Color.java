package common;

import java.io.Serializable;

/**
 * Перечисление допустимых цветов.
 * Используется для определения цветовых характеристик объектов в коллекции.
 *
 * @author Алиса
 * @version 1.0
 */
public enum Color implements Serializable {
    /** Синий цвет */
    BLUE,

    /** Желтый цвет */
    YELLOW,

    /** Белый цвет */
    WHITE;
}
