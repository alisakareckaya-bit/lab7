package client.interfaces;

import common.CommandPacket;

/**
 * Базовый интерфейс для реализации паттерна «Команда».
 * Все доступные пользователю команды приложения должны реализовывать этот интерфейс.
 *
 * @author Алиса
 * @version 1.0
 */
public interface Comand {

    /**
     * Основной метод выполнения логики команды.
     *
     * @param args массив строковых аргументов, переданных вместе с командой
     *             (может быть пустым, если аргументы не требуются).
     * @return {@code true}, если выполнение команды завершилось с состоянием,
     *         требующим особого внимания (например, ошибка скрипта или успех очистки);
     *         {@code false} в обычных случаях выполнения.
     */
    CommandPacket implementCommand(String[] args, String log, String pw);

}
